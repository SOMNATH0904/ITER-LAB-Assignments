'''What is the significance of file modes ('r', 'w', 'a', 'b') in Python?'''


'''
File modes in Python determine how a file is opened and used:

-->'r' (read): Opens the file for reading. The file must exist.
-->'w' (write): Opens the file for writing, truncating it if it exists or creating a new file.
-->'a' (append): Opens the file for writing, appending to the end if it exists or creating a new file.
-->'b' (binary): Used with other modes (e.g., 'rb', 'wb') to read or write binary data instead of text.'''